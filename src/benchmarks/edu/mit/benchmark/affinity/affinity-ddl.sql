
CREATE TABLE SUPPLIERS (
        SUPPLIER_KEY BIGINT NOT NULL,
        FIELD1 VARCHAR(100), 
        FIELD2 VARCHAR(100),
        FIELD3 VARCHAR(100), 
        FIELD4 VARCHAR(100),
        FIELD5 VARCHAR(100), 
        FIELD6 VARCHAR(100),
        FIELD7 VARCHAR(100), 
        FIELD8 VARCHAR(100),
        FIELD9 VARCHAR(100), 
        FIELD10 VARCHAR(100),
        PRIMARY KEY (SUPPLIER_KEY)
);


CREATE TABLE PRODUCTS (
        PRODUCT_KEY BIGINT NOT NULL,
        FIELD1 VARCHAR(100), 
        FIELD2 VARCHAR(100),
        FIELD3 VARCHAR(100), 
        FIELD4 VARCHAR(100),
        FIELD5 VARCHAR(100), 
        FIELD6 VARCHAR(100),
        FIELD7 VARCHAR(100), 
        FIELD8 VARCHAR(100),
        FIELD9 VARCHAR(100), 
        FIELD10 VARCHAR(100),
        PRIMARY KEY (PRODUCT_KEY)
);

CREATE TABLE PARTS (
        PART_KEY BIGINT NOT NULL,     
        FIELD1 VARCHAR(100), 
        FIELD2 VARCHAR(100),
        FIELD3 VARCHAR(100), 
        FIELD4 VARCHAR(100),
        FIELD5 VARCHAR(100), 
        FIELD6 VARCHAR(100),
        FIELD7 VARCHAR(100), 
        FIELD8 VARCHAR(100),
        FIELD9 VARCHAR(100), 
        FIELD10 VARCHAR(100),
        PRIMARY KEY (PART_KEY)
);

CREATE TABLE SUPPLIES (
        PART_KEY BIGINT NOT NULL REFERENCES PARTS(PART_KEY),   
        SUPPLIER_KEY BIGINT NOT NULL REFERENCES SUPPLIERS(SUPPLIER_KEY), 
        
);

CREATE TABLE USES (
        PART_KEY BIGINT NOT NULL REFERENCES PARTS(PART_KEY),   
        PRODUCT_KEY BIGINT NOT NULL REFERENCES PRODUCTS(PRODUCT_KEY), 
);

           
